<div class="InnerHeading"> Glutes stretch  </div>
Start with this if you do zero or less physical activity. After a few weeks, you can progress to the advanced level. 
<br><br>
	
Easy -
<ul class="bullets">	
	<li>Sit on a chair with both feet flat on the ground. </li>
	<li>Cross your right leg over the left knee and bend forward at the hips. </li>
	<li>Try to touch the ground with your hands. </li>
<li>Once you reach your maximum limit, stay there for 10-15 seconds and then pulse a little to try to go further down. Hold this pose for 15 seconds and then release.</li>
	<li>Now, cross the left leg over the right knee and repeat the same. </li>
	<li>Do this exercise 2 times for each side. </li>
	</ul>
