Advanced - 
<ul class="bullets">	
<li>Sit with your legs crossed on the mat but with one leg in front of the other flat on the ground.</li> 
<li>Stretch your arms forward and start bending forward from the hip, keeping the back straight. </li>
	<li>Go slowly and try to touch the ground with your palms. </li>
	<li>Stay in this stance for 15 seconds and slowly return to the original position.</li> 
	<li>Change the leg keeping the opposite leg in front now and repeat the process. </li>
	<li>Do this exercise 2 times for each side. </li>
	</ul>
