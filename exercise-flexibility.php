<div class="eq_wid">	
<div class="InnerHeading"> Cat and Camel pose  </div>

<ul class="bullets">	
<li>Start with being on all fours. The palms should be on the floor in line with your shoulders while the elbows remain straight. The hips should be in line with the knees which are bent at 90 degrees.</li> 
<li>Exhale and arch your back as high as possible (like a cat) and pull the abdomen in. Tuck your chin in towards your chest.</li> 
<li>Inhale and while looking up at the ceiling, push your back down as much as possible. (like a camel)</li>
<li>Alternate between these 2 stances 5 times without any breaks. Go with the flow of your breath. Do not hold any one stance. </li>

	</ul>
