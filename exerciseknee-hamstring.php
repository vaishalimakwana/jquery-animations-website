<div class="InnerHeading"> Kick Backs  </div>

<strong>Purpose: </strong>Improve the strength of hamstrings and mobility of the knee.<br><br>
<strong>Starting Position:</strong> Stand up straight holding on to something stable e.g., chair or table.<br><br> 
<strong>Action:</strong> Lift your right foot up as far as you can towards your hips, bending the knee. Hold for 3-5 seconds. Slowly, lower to starting position. Repeat with the left leg.<br><br>
<strong>Repetition:</strong> Do 5-25 times, 2 times daily <br><br>
<strong>Progression:</strong> Add a weight e.g., shoe or ankle weight under the guidance of a therapist. <br><br>

<strong>Note:</strong><ul class="bullets"><li>Don’t bend forwards - keep your body upright.</li> 
<li>Keep your knees in line with each other- don’t let your thigh come forwards.</li>
</ul>
