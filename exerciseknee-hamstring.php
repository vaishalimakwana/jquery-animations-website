<div class="InnerHeading">  The Bridge  </div>


<strong>Purpose:</strong> Excellent knee strengthening exercise for the hamstrings, quads and buttock muscles.<br><br> 
<strong>Starting Position:</strong> Lie on your back with both knees bent about 90° and your feet on the floor/bed.<br><br> 
<strong>Action:</strong> Clench your buttocks and lift your bottom off the bed as high as you can without arching your back. The back appears like a bridge between your upper and lower body. Hold for 3-5 seconds and slowly lower to starting position.<br><br>
<strong>Repetitions:</strong> Repeat 10-15 times, 1-2 times daily.<br><br>

<strong>Note:</strong>  
	<ul class="bullets">
	<li>Keep your back straight – don’t let it arch as you lift up, your buttock muscles should be doing the work</li> 
	<li>Don’t hold your breath – keep breathing normally.</li>
    </ul>


