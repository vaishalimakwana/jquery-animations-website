<div class="InnerHeading"> Buttock Kicks </div>

<strong>Purpose:</strong> Strengthen hamstrings, increase knee mobility (flexion).<br><br>
<strong>Starting position:</strong> Lie on your abdomen (on the floor or bed) with your legs straight.<br><br>
<strong>Action:</strong> Bend your right knee to bring the foot towards your buttocks as far as you can. Slowly, return to the starting position. Repeat with your left leg.<br><br>
<strong>Repetition:</strong> Repeat it 10 – 15 times for each leg, 2 times daily.<br><br>
<strong>Progression:</strong> Add a weight (E.g., a shoe or ankle weight, to be added under the guidance of a therapist).
