			<div class="dietTopBg">
				<div class="dietMidBg">
				<div>
				Weaning typically begins from 4-6 months of age.<br>
Here we aim to reduce breastfeeding and start introducing the infant to solid food as well as to formulate an eating pattern for the 4 major meals of the day which  includes : Breakfast, Lunch, Evening Snack &amp; Dinner, which they can adhere to as they grow up. <br>
Some pointers to begin with:<br><br>
<ul class="bullets">
	<li>The baby might spit out anything new he/she tries; offer them the same food again after a few minutes.If they still persist at spitting, try offering it again after a few days.</li><br>
	<li>Introduce only one new food item at a time. Keep a gap of 2-3 days between every new food item introduced.</li><br>
	<li>Do not add salt to the baby’s food until 8 months of age.</li><br>
	<li>Introduce foreign proteins like honey, egg, meats and fish after the age of 1 year.</li><br>
	<li>Continue breastfeeding till 1 year of age.</li>
</ul>					
<br>					
Following is a planned dietary chart to guide you through the process of weaning -					
<div class="eq_wid">	
<div class="InnerHeading">Week 1 - Introducing Fruits </div>
	<ul class="bullets">
	<li>Introduce the infant to fruits for breakfast. </li>
 
	<li>Start with softer fruits like banana, chikoo, mango, papaya and custard apple. </li>
 
	<li>Mash the fruits well and offer the baby a small spoonful. At first the baby might spit it out but try giving it again.</li>
	<li>Offer it to the baby daily for the next couple of days till they gradually acquire the taste. </li>
	<li>If the baby takes it well, you can also introduce stewed apples and pears after a few days.</li> 
</ul>

</div>
<div class="eq_wid">	
<div class="InnerHeading">Week 2 - Introducing Nachni </div>
<ul class="bullets">
	<li>Continue the fruits for breakfast and introduce nachni (ragi) for lunch, a calcium-rich cereal. It is beneficial for the baby’s growth, especially during the teething period. </li>	
	<li>Boil the <em>nachni</em> in water and add jaggery to this. Initially keep this porridge watery. Then as the  baby starts taking it well, over the next few days you can make the consistency thicker. </li>
	<li>As mentioned, introduce small spoonfuls initially. Keep offering it to the baby for the next few days till he/she acquires the taste. Increase from a few spoonfuls to one small bowl over time if he/she takes it well.</li>
	<li>Set a time for the breakfast and lunch, so that a routine is set. The rest of the time continue  breastfeeding for the first 2 weeks. 
     If the baby is hungry between the feeds, you can try giving fruits again. </li>
</ul>


</div>
					
<div class="eq_wid">	
<div class="InnerHeading">Week 3 - Introducing Rice </div>

<ul class="bullets">
	<li>Continue with <em>nachni</em> for breakfast.</li>
	<li>For lunch, introduce rice - You can start with brown or red rice.</li>
	<li>Cook it with yellow moong dal and mash well.</li>
	<li>For the first few days make this khichdi only with yellow moong dal. 
You can add jaggery to it. However, do not add any salt.</li>
	<li>After 2 days you can add boiled and mashed vegetables to the khichdi, such as dudhi, spinach, carrots, sweet potatoes, tomatoes, pumpkin, and beetroot.</li>
	<li>Introduce only one vegetable at a time.</li>
	<li>The evening meal can either be fruits or a breastfeed.</li>
</ul>


</div>					
					
					
					
<div class="eq_wid">	
<div class="InnerHeading">Week 4 -  Introducing different dals and vegetables</div>
<ul class="bullets">
	<li>For breakfast, continue the <em>nachni.</em></li>
	<li>Offer the rice khichdi for lunch. </li>
 
	<li>You can now substitute the yellow moong dal with green moong, tur, masoor dal. </li>
 
<li>Continue to introduce the new vegetables as explained in the week 3 diet . Introduce Only one new item in a day and maintain a gap of two to three days between every new item introduced.</li>
 
	<li>	For dinner, give the baby fruits. </li>
 
	<li>	Continue the breastfeeds for the rest of the day. </li>

</ul>


</div>

<div class="eq_wid">	
<div class="InnerHeading">Week 5 - Introducing Wheat</div>
	<ul class="bullets">
		<li>For breakfast continue giving nachni.</li>
 
	 
<li>For lunch, introduce broken wheat daliya. Initially, combine it with jaggery and  later with boiled and mashed vegetables. </li>
 
		<li>You can add the dals which you have already introduced earlier. </li>
 
	<li>Shift the rice khichdi to dinner, as the baby has been eating it since two weeks, they are accustomed to the taste and will be able to take it well even in the evening.Give fruits in the evening and between meals whenever hungry.</li>
 </ul>
You should continue the breastfeeding as it is, not for the calories but for the antibodies and the immunity that breast milk provides the baby.




</div>					
					
<div class="eq_wid">
	<div class="InnerHeading">Week 6 onwards</div>

<ul class="bullets">
	<li>Gradually reduce the mashing after a few weeks.</li> 

	<li>Move to grinding foods with a mixer/blender, then shift to mashing with hands.</li>

<li>Gradually, start separating out the rice, dal and vegetables. Introduce vegetables such as cabbage, cauliflower, peas, capsicum, and methi after a few months.</li> 

	<li>Add pulses such as chana, rajma, and kali dal to the baby’s diet after a few months. </li>
	

</ul>			
					
</div>


					
<div class="eq_wid">
	<div class="InnerHeading">At 8 months of age</div>

	<ul class="bullets">
	
	<li>You can start offering the baby foods of different tastes that are cooked at home like dosa, uttapam, white dhokla (without baking soda) with batter made at home.</li>

		<li>You may offer poha and upma for breakfast.</li> 

		<li>Continue nachni either at breakfast or in the evening snack as it helps in teething.</li> 

		<li>Allow the baby to pick up and feed on its own, even if they are dropping a lot of the food</li>

<li>By 1 year of age the baby should be eating a full meal, which includes a cereal (rice or wheat), protein (dal, chana, chawli, rajma) and a vegetable, without spices and low in salt.</li> 

<li>The amount of salt should be less than 1 gram between the age of 7 to 12 months.  A pinch of salt has 0.25 g.</li>

<li>DO NOT GIVE the baby spices, eggs, meat , fish , or any other foreign proteins including honey before 1 year of age.</li> 

<li>DO NOT GIVE any packaged foods such as cream biscuits, chips, chocolates, cakes, white sugar, etc.</li> 

	
	
	
	</ul>			
					
